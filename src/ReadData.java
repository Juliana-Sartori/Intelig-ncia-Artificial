        public static double[][][] convertToTridimensionalArray(String caminhoDoArquivo) {
            // Arquivo de entrada com os dados
            List<double[][]> listaBalance = new ArrayList<>();

            try (BufferedReader br = new BufferedReader(new FileReader(caminhoDoArquivo))) {
                String linha;
                while ((linha = br.readLine()) != null) {
                    // Divide a linha nos valores
                    String[] partes = linha.split(",");

                    // Mapeia a letra inicial para o array correspondente
                    double[] valoresTipo = new double[3];
                    switch (partes[0]) {
                        case "R":
                            valoresTipo = new double[]{1, 0, 0};
                            break;
                        case "L":
                            valoresTipo = new double[]{0, 1, 0};
                            break;
                        case "B":
                            valoresTipo = new double[]{0, 0, 1};
                            break;
                    }

                    // Cria o array para os valores numéricos
                    double[] valoresNumericos = new double[4];
                    for (int i = 0; i < 4; i++) {
                        valoresNumericos[i] = Double.parseDouble(partes[i + 1]);
                    }

                    // Adiciona uma nova camada à matriz tridimensional
                    listaBalance.add(new double[][]{valoresNumericos, valoresTipo});
                }
            } catch (IOException e) {
                System.out.println(Arrays.toString(e.getStackTrace()));
            }
            // Converte a lista para uma matriz tridimensional
            double[][][] balance = listaBalance.toArray(new double[0][][]);
            return balance;
        }

        public static void printMatriz(double[][][] matriz) {
            // Exibe a matriz tridimensional para verificação
            for (double[][] camada : matriz) {
                System.out.print("{");
                for (double[] linha : camada) {
                    System.out.print("{");
                    for (double valor : linha) {
                        System.out.print(valor + ", ");
                    }
                    System.out.print("} ");
                }
                System.out.println("}");
            }
        }

